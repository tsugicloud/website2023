<h2>title: Google Classroom</h2>
<p><strong>Using TsugiCloud in Google Classroom</strong></p>
<p>It is relatively simple to use TsugiCloud in Google Classroom.</p>
<ol>
<li>
<p>Go to the App Store and login with your Google Account</p>
</li>
<li>
<p>From Settings, connect your Google account on TsugiCloud to your Google Classroom</p>
</li>
<li>
<p>Then go to the Tsugi App Store - at that point, if you have successfully connected to one or more Google Classrooms, you should see the Google Classroom icon next to each of the applications in the store.  </p>
</li>
<li>
<p>When you click on the Google Classroom icon <img src="https://www.gstatic.com/classroom/logo_square_48.svg" style="height: 2rem;"> you will be prompted for a Google Classroom course to install the tool.</p>
</li>
<li>
<p>Once you install a tool, it should appear in your Classroom &quot;wall&quot;.  Launch the tool as the instructor to see if the tool needs any post-install configuration.</p>
</li>
<li>Once the tools is installed and configured, students can launch the tool. If the tool produces grades they will be sent back to the Classroom grade book.  Note that you need a student account to test grades going back to Classroom.  Grades do not flow for Instructors.</li>
</ol>
<p>If you want to experiment with this, you need two @gmail.com accounts - one to be the teacher and one to be the student.  Simply go to classroom.google.com and create a course on the teacher account and invite your other gmail account to the course.  You can install tools on the teacher account and get graded from the student account.</p>
<p>You may or may not be able to use Classroom from an &quot;enterprise&quot; Google account.  Your GSuite adminstrator must explicitly allow a domain to use Google Classroom. </p>
