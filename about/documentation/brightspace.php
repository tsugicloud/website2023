<h2>title: Brightspace </h2>
<p><strong>Using TsugiCloud in Brightspace</strong></p>
<ol>
<li>
<p>Go to the TsugiCloud App Store and login with your Google Account</p>
</li>
<li>Go to the Settings and select &quot;Manage LMS Access Keys&quot;.  Request an LTI 1.x key and wait for the email approving the key and giving your key and secret.  Keep these safe.</li>
</ol>
<p>Brightspace supports the IMS Content Item standard so you can install this site as an &quot;App Store&quot; / &quot;Learning Object Repository&quot; using this url and your key and secret:</p>
<p><a href="https://www.tsugicloud.org/tsugi/lti/store/">https://www.tsugicloud.org/tsugi/lti/store/</a></p>
<p>The documentation to install a tool in Brightspace and enabing Content Item support is:</p>
<p><a href="https://documentation.brightspace.com/EN/le/lti/admin/adding_content_item_messagel_certified_third_party_tool.htm" target="_blank">
Adding a Content-Item Message certified third-party tool</a>.</p>